# coding=utf-8
# 带符号整数
print('1%d'%123)
# 
print('1%i'%123)
# 八进制
print('1%o'%123)
# 十进制
print('1%u'%123)
# 十六进制
print('1%x'%123)
print('1%X'%123)
# 科学计数
print('1%e'%123)
print('1%E'%123)
# 浮点数
print('1%f'%123)
print('1%F'%123)
# 浮点数字(根据值的大小采用%e或%f)
print('1%g'%123)
print('1%G'%123)
# 字符及其ASCII码
print('1%c'%123)
# 
print('1%r'%123)
# 字符串
print('1%s'%123)
# 没有意义
print('1%%')
print('%10s'%123456)

